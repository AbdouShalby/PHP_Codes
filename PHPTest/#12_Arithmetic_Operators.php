<?php

/*
 * Arithmetic Operators
 * [ + ] Addition
 * [ - ] Subtraction
 * [ * ] Multiplication
 * [ / ] Division
 * [ % ] Modulus
 */
$var1 = 100;
$var2 = 200;

echo $var1 + $var2; // Addition 100 + 200 = 300
echo "<br>";
echo $var1 - $var2; // Subtraction 100 - 200 = -100
echo "<br>";
echo $var1 * $var2; // Multiplication 100 * 200 = 20000
echo "<br>";
echo $var1 / $var2; // Division 100 / 200 = 0.5
echo "<br>";
echo $var1 % $var2; // Modulus 100 % 200 = 100

